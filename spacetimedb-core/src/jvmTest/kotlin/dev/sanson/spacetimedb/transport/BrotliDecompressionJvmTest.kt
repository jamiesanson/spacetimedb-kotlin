package dev.sanson.spacetimedb.transport

import dev.sanson.spacetimedb.protocol.Compression
import kotlin.test.Test
import kotlin.test.assertContentEquals

class BrotliDecompressionJvmTest {

    @Test
    fun `brotli decompression of hello message`() {
        val original = "Hello, SpacetimeDB!".encodeToByteArray()
        // Pre-compressed with Python brotli.compress()
        val compressed = byteArrayOf(
            0x0B, 0x09, 0x80.toByte(), 0x48, 0x65, 0x6C, 0x6C, 0x6F,
            0x2C, 0x20, 0x53, 0x70, 0x61, 0x63, 0x65, 0x74,
            0x69, 0x6D, 0x65, 0x44, 0x42, 0x21, 0x03,
        )
        assertContentEquals(original, decompressBrotli(compressed))
    }

    @Test
    fun `brotli tag with compressed payload decompresses correctly`() {
        val original = byteArrayOf(10, 20, 30, 40, 50)
        val compressed = byteArrayOf(
            0x0B, 0x02, 0x80.toByte(), 0x0A, 0x14, 0x1E, 0x28, 0x32, 0x03,
        )
        val raw = byteArrayOf(Compression.Brotli.tag) + compressed
        assertContentEquals(original, decompressServerMessage(raw))
    }

    @Test
    fun `brotli empty payload decompresses to empty array`() {
        val compressed = byteArrayOf(0x3B)
        assertContentEquals(byteArrayOf(), decompressBrotli(compressed))
    }

